// Program 5_9
// Type conversions

#include <iostream>
using namespace std;

int main() {
	int a = 3;
	float b = 2.6;
	bool c = true;

	cout << float(a) << "  " << (float)a << endl;
	cout << bool(a) << "  " << (bool)a << endl;
	cout << int(b) << "  " << (int)b << endl;
	cout << bool(b) << "  " << (bool)b << endl;
	cout << int(c) << "  " << (int)c << endl;
	cout << float(c) << "  " << (float)c << endl;







}